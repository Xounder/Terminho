    static String apagaIgual(String resp, int[] verLetra) {
        String format;
        char[] letras = {resp.charAt(0), resp.charAt(1), resp.charAt(2), resp.charAt(3), resp.charAt(4)};
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {
                if (verLetra[i] == 1) {
                    letras[i] = '-';
                }
                if (letras[i] == letras[j] && i != j) {
                    letras[j] = '-';
                }
            }
        }
        format = "" + letras[0] + letras[1] + letras[2] + letras[3] + letras[4];
        return format;
    }
    
    static int[] verificaLetra(String resp, String termo, int[] verLetra) {
        for (int i = 0; i < 5; i++) {
            if (resp.charAt(i) == termo.charAt(i)) { //verifica se a letra dita esta na mesma posição
                verLetra[i] = 1;
            } else {
                verLetra[i] = 0;
            }
        }
        return verLetra;
    }
    
    static int[] verificaExiste(String respmod, String termo, int[] verLetra) {
        for (int i = 0; i < 5; i++) {
            for (int j = 0; j < 5; j++) {    //verifica se a letra existe em outra posição
                if (respmod.charAt(i) == termo.charAt(j) && i != j && verLetra[i] != 1 && verLetra[j] != 1) {
                    verLetra[i] = 2;
                }
            }
        }
        return verLetra;
    }
    
    static int[] ajustaValores(String termo, String resp, int[] verLetra){
        //verifica se está na posição correta
        verLetra = verificaLetra(resp, termo, verLetra);
        //retira os itens que ja estão na posição correta e letras iguais
        String respmod = apagaIgual(resp, verLetra);
        //verifica novamente com as mudanças, para saber se existe a letra na palavra
        verLetra = verificaExiste(respmod, termo, verLetra);
        return verLetra;
    }

