    static String replaceCaracteres(String palavra) {
        return palavra.replace('ã', 'a').replace('á', 'a').replace('â', 'a').replace('ê', 'e').replace('é', 'e').replace('ë', 'e')
                .replace('õ', 'o').replace('ó', 'o').replace('ô', 'o').replace('í', 'i').replace('î', 'i').replace('ï', 'i').
                replace('ú', 'u').replace('û', 'u').replace('ü', 'u').replace('ç', 'c').toUpperCase();
    }
    
