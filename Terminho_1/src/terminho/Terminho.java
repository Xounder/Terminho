    private void respostaPlayerKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_respostaPlayerKeyReleased
        // TODO add your handling code here:
        if (respostaPlayer.getText().length() == 5 && evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String resp = replaceCaracteres(respostaPlayer.getText());
            
            verLetra = ajustaValores(termo, resp, verLetra);
           
            switch(contLinha){
                case 0:
                    writeLetras(jTent11, jTent12, jTent13, jTent14, jTent15, verLetra, resp);
                    break;
                case 1:
                    writeLetras(jTent21, jTent22, jTent23, jTent24, jTent25, verLetra, resp);
                    break;
                case 2:
                    writeLetras(jTent31, jTent32, jTent33, jTent34, jTent35, verLetra, resp);
                    break;
                case 3:
                    writeLetras(jTent41, jTent42, jTent43, jTent44, jTent45, verLetra, resp);
                    break;
                case 4:
                    writeLetras(jTent51, jTent52, jTent53, jTent54, jTent55, verLetra, resp);
                    break;
                case 5:
                    writeLetras(jTent61, jTent62, jTent63, jTent64, jTent65, verLetra, resp);
                    break;
                default:
                    break;
            }
            respostaPlayer.setText("");
            contLinha++;
                       
            if (verLetra[0] == 1 && verLetra[1] == 1 && verLetra[2] == 1 && verLetra[3] == 1 && verLetra[4] == 1) {
                int escolha = JOptionPane.showConfirmDialog(this, "Você venceu!\n\nA palavra certa é: " + 
                        mostraTermo + "\n\nDeseja Continuar?", "Vitória!!!", 2);
                if (escolha == 0) {
                    newGame();
                } else {
                    dispose();
                }
                vit++;
                sequenciaVitoria.setText("Sequencia de Vitórias: " + vit);
                sequenciaVitoria.setVisible(true);
            } else if (!" __".equals(jTent61.getText())) {
                int escolha = JOptionPane.showConfirmDialog(this, "Você perdeu!\n\n A palavra certa é: " + 
                        mostraTermo + "\n\nDeseja Continuar?", "Derrota!", 2);
                vit = 0;
                sequenciaVitoria.setVisible(false);

                if (escolha == 0) {
                    newGame();
                } else {         
                    new Menu().setVisible(true);
                    dispose();
                }
            }
        }

        //Mudança no contador de letras
        if (respostaPlayer.getText().length() == 0) {
            cont = 0;
        }
        conTentantiva.setVisible(true);
        conTentantiva.setText("" + cont);
        if (cont != 5) {
            conTentantiva.setForeground(Color.red);
        } else {
            conTentantiva.setForeground(Color.blue);
        }


    }//GEN-LAST:event_respostaPlayerKeyReleased

    private void respostaPlayerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_respostaPlayerKeyPressed
        // TODO add your handling code here:
        if (cont >= 1 && respostaPlayer.getText().length() > 0 && evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            cont--;

        } else if (evt.getKeyCode() != KeyEvent.VK_BACK_SPACE && evt.getKeyCode() != KeyEvent.VK_DELETE
                && evt.getKeyCode() != KeyEvent.VK_UP && evt.getKeyCode() != KeyEvent.VK_LEFT
                && evt.getKeyCode() != KeyEvent.VK_RIGHT && evt.getKeyCode() != KeyEvent.VK_DOWN && evt.getKeyCode() != KeyEvent.VK_ENTER) {

            cont++;
        }
    }//GEN-LAST:event_respostaPlayerKeyPressed

