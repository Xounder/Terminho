    private void respostaPlayerKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_respostaPlayerKeyPressed
        // TODO add your handling code here:
        if (cont >= 1 && respostaPlayer.getText().length() > 0 && evt.getKeyCode() == KeyEvent.VK_BACK_SPACE) {
            cont--;
        } else if (evt.getKeyCode() != KeyEvent.VK_BACK_SPACE && evt.getKeyCode() != KeyEvent.VK_DELETE
                && evt.getKeyCode() != KeyEvent.VK_UP && evt.getKeyCode() != KeyEvent.VK_LEFT
                && evt.getKeyCode() != KeyEvent.VK_RIGHT && evt.getKeyCode() != KeyEvent.VK_DOWN && evt.getKeyCode() != KeyEvent.VK_ENTER) {
            
            cont++;
        }
    }//GEN-LAST:event_respostaPlayerKeyPressed

